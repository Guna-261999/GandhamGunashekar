import java.util.Scanner;

public class OddNum {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in) ;
		System.out.print("enter the Number :");
		int n = sc.nextInt();
		for (int i = 1; i <= n; i++) {
			System.out.print(i + " ");
			
		}
	}

}
